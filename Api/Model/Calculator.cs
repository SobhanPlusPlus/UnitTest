namespace Api.Model
{
    public class Calculator
    {
    public int Sum(int n1, int n2)
        {
            //return n1/n2;
            return n1+n2;
        }

         public int Sub(int n1, int n2)
        {
            return n1-n2;
        }

               public int Division(int n1, int n2)
        {
            return n1/n2;
        }

           public int Multiplication(int n1, int n2)
        {
            return n1+n2;
        } 
    }

 
